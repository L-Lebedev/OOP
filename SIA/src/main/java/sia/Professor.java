    public String getWorkDaysString() {
        String[] dayNames = {"Lunes", "Martes", "Miércoles", "Jueves", "Viernes", "Sábado", "Domingo"};
        String output = "";
        for (int i = 0; i < this.workDays.length; i++) {
            if (this.workDays[i]) {
                output += dayNames[i] + " ";
            }
        }
        return output.trim();
    }
    
